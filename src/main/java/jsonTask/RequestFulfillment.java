package jsonTask;

public class RequestFulfillment {
    private String time;
    private int count;

    public RequestFulfillment() {
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
